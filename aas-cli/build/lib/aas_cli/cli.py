"""Command-line interface for aas-cli.

    aas-cli servers
    aas-cli init
    aas-cli list    --server NAME [--type shell|submodel|concept-description] [--registry] [--ids-only]
    aas-cli upload  --server NAME --file PATH [--ignore-duplicates]
    aas-cli download --server NAME [--type shell|submodel --id ID] [--format json|xml|aasx] [--out PATH]
    aas-cli delete  --server NAME --type shell|submodel --id ID
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

import yaml

from .client import ENTITY_PATHS, BasyxClient
from .config import get_server, load_servers


def cmd_servers(args: argparse.Namespace) -> None:
    servers = load_servers(args.config)
    if not servers:
        print("No servers configured.")
        return
    for name, server in sorted(servers.items()):
        registry = f"  registry={server.registry_url}" if server.registry_url else ""
        print(f"{name:<15} {server.url}{registry}")


def cmd_init(args: argparse.Namespace) -> None:
    out_path = Path(args.output)
    if out_path.exists() and not args.force:
        raise SystemExit(f"{out_path} already exists (use --force to overwrite).")

    env_values: dict[str, str] = {}
    env_path = Path(args.env_file)
    if not env_path.exists() and args.env_file == ".env":
        # aas-cli commonly lives in a subdirectory of the repo it serves.
        parent_candidate = Path("..") / ".env"
        if parent_candidate.exists():
            env_path = parent_candidate
    if env_path.exists():
        for line in env_path.read_text().splitlines():
            line = line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            key, _, value = line.partition("=")
            env_values[key.strip()] = value.strip()

    host = env_values.get("HOST_ADDRESS", "<YOUR-IP>")
    shared = env_values.get("SHARED_ADDRESS", host)

    config = {
        "servers": {
            "local": {
                "url": f"https://{host}",
                "registry_url": f"https://{shared}",
                "verify_tls": False,
                "keycloak": {
                    "url": f"https://{shared}/auth",
                    "realm": "basyx",
                    "client_id": "basyx-api",
                    "client_secret": "basyx-api-secret",
                },
                "username": "basyx-admin",
                "password": "basyx-admin",
            },
        }
    }

    out_path.write_text(
        "# Generated by 'aas-cli init' from this repo's .env (join.py output).\n"
        "# Add one entry per additional server you want to reach (teammates',\n"
        "# other partners') below -- see servers.example.yaml for the shape.\n"
        + yaml.safe_dump(config, sort_keys=False)
    )
    print(f"Wrote {out_path}. Try: aas-cli servers")


def cmd_list(args: argparse.Namespace) -> None:
    server = get_server(args.server, args.config)
    client = BasyxClient(server)
    entities = client.list_registry(args.type) if args.registry else client.list_entities(args.type)
    if args.ids_only:
        for entity in entities:
            print(entity.get("id"))
    else:
        print(json.dumps(entities, indent=2))


def cmd_upload(args: argparse.Namespace) -> None:
    server = get_server(args.server, args.config)
    file_path = Path(args.file)
    if not file_path.exists():
        raise SystemExit(f"File not found: {file_path}")
    BasyxClient(server).upload_environment(file_path, ignore_duplicates=args.ignore_duplicates)
    print(f"Uploaded {file_path} to '{server.name}'.")


def cmd_download(args: argparse.Namespace) -> None:
    server = get_server(args.server, args.config)
    client = BasyxClient(server)
    aas_ids = [args.id] if args.id and args.type == "shell" else []
    submodel_ids = [args.id] if args.id and args.type == "submodel" else []
    content = client.download_serialization(aas_ids=aas_ids, submodel_ids=submodel_ids, fmt=args.format)
    out_path = Path(args.out) if args.out else Path(f"download.{args.format}")
    out_path.write_bytes(content)
    print(f"Saved to {out_path}")


def cmd_delete(args: argparse.Namespace) -> None:
    server = get_server(args.server, args.config)
    BasyxClient(server).delete_entity(args.type, args.id)
    print(f"Deleted {args.type} '{args.id}' from '{server.name}'.")


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="aas-cli", description="Interact with BaSyx AAS servers (your own or others') via REST."
    )
    parser.add_argument("--config", help="Path to servers.yaml (default: auto-discover)")
    subparsers = parser.add_subparsers(dest="command", required=True)

    p_servers = subparsers.add_parser("servers", help="List configured servers")
    p_servers.set_defaults(func=cmd_servers)

    p_init = subparsers.add_parser("init", help="Generate servers.yaml from this repo's .env")
    p_init.add_argument("--output", default="servers.yaml")
    p_init.add_argument("--env-file", default=".env")
    p_init.add_argument("--force", action="store_true")
    p_init.set_defaults(func=cmd_init)

    p_list = subparsers.add_parser("list", help="List shells/submodels/concept-descriptions on a server")
    p_list.add_argument("--server", required=True)
    p_list.add_argument("--type", choices=list(ENTITY_PATHS), default="shell")
    p_list.add_argument("--registry", action="store_true", help="Query the registry instead of the repository")
    p_list.add_argument("--ids-only", action="store_true")
    p_list.set_defaults(func=cmd_list)

    p_upload = subparsers.add_parser("upload", help="Upload an environment file (AASX/JSON/XML)")
    p_upload.add_argument("--server", required=True)
    p_upload.add_argument("--file", required=True)
    p_upload.add_argument("--ignore-duplicates", action="store_true")
    p_upload.set_defaults(func=cmd_upload)

    p_download = subparsers.add_parser("download", help="Download a shell/submodel, or the whole environment")
    p_download.add_argument("--server", required=True)
    p_download.add_argument("--type", choices=["shell", "submodel"], default="shell")
    p_download.add_argument("--id", help="Identifier to download (omit for the whole environment)")
    p_download.add_argument("--format", choices=["json", "xml", "aasx"], default="json")
    p_download.add_argument("--out", help="Output file path")
    p_download.set_defaults(func=cmd_download)

    p_delete = subparsers.add_parser("delete", help="Delete a shell or submodel")
    p_delete.add_argument("--server", required=True)
    p_delete.add_argument("--type", choices=["shell", "submodel"], required=True)
    p_delete.add_argument("--id", required=True)
    p_delete.set_defaults(func=cmd_delete)

    return parser


def main(argv: list[str] | None = None) -> None:
    args = build_parser().parse_args(argv)
    args.func(args)


if __name__ == "__main__":
    main()
